function hashString(s: string) {
  let h = 0
  for (let i = 0; i < s.length; i++) h = (h * 31 + s.charCodeAt(i)) >>> 0
  return h
}

/** Deterministic “quote of the day” by date + tone (same across devices). */
export function getDailyQuote(dateIso: string, tone: QuoteTone): string {
  const list = MOTIVATION_QUOTES[tone]
  if (!list?.length) return ''
  const idx = hashString(`${tone}:${dateIso}`) % list.length
  return list[idx] ?? ''
}
